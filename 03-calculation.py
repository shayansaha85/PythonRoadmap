
number1 = 56
number2 = 4

# addition
additionResult = number1 + number2
print(additionResult)

# subtraction
subtractionResult = number1 - number2
print(subtractionResult)

# multiplication
multResult = number1 * number2
print(multResult)

# divison : decimal, whole
# 5/2 = 2.5 --> integer : 2, decimal : 2.5

# division to get integer
integerDivisionResult = number1 // number2
print(integerDivisionResult)

# division to get decimal
floatDivisionResult = number1 / number2
print(floatDivisionResult)

# MODULUS : %
# 2 | 5 | 2
#   | 4 |

modulus_res = number1 % number2
print(modulus_res)


# special calculations
# self calculation

x = 3
# +=, -=, *=, /= >>> Augmented operators
x *= 1
x *= 5

print(x)

# superscript / power
print(3**7)