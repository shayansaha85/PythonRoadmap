
# Grade A -- Very good
# Grade B -- Good
# Grade C -- Soso
# Grade F -- Failed

grade = 'F'

if grade == 'A':
      print('Very good')
      
elif grade == 'B':
      print('Good')
      
elif grade == 'C':
      print('Soso')
      
elif grade == 'F':
      print('Failed')
      
else:
      print('Inappropriate grade')      